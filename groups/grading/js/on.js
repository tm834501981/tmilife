function getshow(){
	var show = document.getElementById('show');
	var x = document.getElementById('x');
	show.style.display = 'block';
	x.style.display = 'block';
}
function gethidden(){
	var show = document.getElementById('show');
	var x = document.getElementById('x');
	show.style.display = 'none';
	x.style.display = 'none';
}